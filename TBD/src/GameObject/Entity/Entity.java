package GameObject.Entity;

public class Entity {
	private int ID;
	private float X, Y;

	public int getID() {
		return ID;
	}

	public void setID(int id) {
		ID = id;
	}

	public float getX() {
		return X;
	}

	public void setX(float x) {
		X = x;
	}

	public float getY() {
		return Y;
	}

	public void setY(float y) {
		Y = y;
	}
}